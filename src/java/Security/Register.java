    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        PrintWriter out = response.getWriter();
        
        String uName = request.getParameter("user_name");
        String uPass = request.getParameter("user_pass");
        String uFName = request.getParameter("first_name");
        String uLName = request.getParameter("last_name");
        String uProfs = request.getParameter("usr_profession");
        boolean uAccountClass = request.getParameter("act_class").equals("pro");
        int uAge = Integer.parseInt(request.getParameter("age"));
        String uGender = request.getParameter("usr_gender");
        
        if(Registration.insertUserData(uName, uPass, uFName, uLName, uProfs, uAccountClass, uAge, uGender))
        {
            System.out.println("Registartion successful!");
            RequestDispatcher rs = request.getRequestDispatcher("mainPage.jsp");
            rs.forward(request, response);
        }
        else
        {
           System.out.println("Registration failed!");
           RequestDispatcher rs = request.getRequestDispatcher("index.jsp");
           rs.include(request, response);
        }
    }

